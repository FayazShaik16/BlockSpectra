export const useTranslation = () => {
  const [currentLang, setCurrentLangState] = useState<string>('en');

  const detectLanguage = (): string => {
    try {
      const browserLang = navigator.language || (navigator.languages && navigator.languages[0]);
      if (browserLang) {
        const baseLang = browserLang.split('-')[0].toLowerCase();
        if (TRANSLATIONS[baseLang]) {
          return baseLang;
        }
      }
    } catch (e) {
      console.error('Failed to detect browser language', e);
    }
    return 'en';
  };

  useEffect(() => {
    const saved = localStorage.getItem('preferred_language');
    if (saved && TRANSLATIONS[saved]) {
      setCurrentLangState(saved);
    } else {
      const detected = detectLanguage();
      setCurrentLangState(detected);
      localStorage.setItem('preferred_language', detected);
    }
  }, []);

  const setLang = (lang: string) => {
    if (TRANSLATIONS[lang]) {
      setCurrentLangState(lang);
      localStorage.setItem('preferred_language', lang);
    }
  };

  const t = (key: string, defaultVal?: string): string => {
    const langDict = TRANSLATIONS[currentLang] || TRANSLATIONS['en'];
    const translated = langDict[key];
    if (translated !== undefined) {
      return translated;
    }
    const englishDict = TRANSLATIONS['en'];
    const englishVal = englishDict[key];
    if (englishVal !== undefined) {
      return englishVal;
    }
    return defaultVal || key;
  };

  return {
    t,
    currentLang,
    setLang,
    supportedLanguages: SUPPORTED_LANGUAGES
  };
};

